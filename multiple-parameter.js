// Substract Two Number By input parameter
function substractTwoNumbers (num1,num2){
    var substract = num1 - num2 ;
    return substract;
}
var num1 = 50;
var num2 = 10;
var result = substractTwoNumbers(num1,num2);
console.log(result);


// Divided Two number by input parameter
function dividedTwoNumbers (num1,num2){
    var substract = num1 / num2 ;
    return substract;
}
var number1 = 50;
var number2 = 10;
var result = dividedTwoNumbers(number1,number2);
console.log(result);